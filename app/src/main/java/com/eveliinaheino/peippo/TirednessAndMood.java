package com.eveliinaheino.peippo;

public class TirednessAndMood {
    private int tiredness;
    private int mood;

    public TirednessAndMood(int tiredness, int mood){
        this.tiredness = tiredness;
        this.mood = mood;
    }

    public int getMood() {
        return mood;
    }

    public int getTiredness() {
        return tiredness;
    }
}
